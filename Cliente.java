public class Cliente {

    private String email;
    private String telefone;
    private String nome;
    private int    idade;
    private double salario;

    public Cliente(String email, String telefone, String nome, int idade, double salario){
        this.email = email;
        this.telefone = telefone;
        this.nome = nome;
        this.idade = idade;
        this.salario = salario;
    }
}
